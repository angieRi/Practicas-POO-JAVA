/**
 *  El libro solo tiene dos métodos: alquilar y devolver, 
 */
public class Libro {

	private boolean alquilar;
	private boolean devolver;
	
	public boolean isAlquilar() {
		return alquilar;
	}
	
	public void setAlquilar(boolean alquilar) {
		this.alquilar = alquilar;
	}
	
	public boolean isDevolver() {
		return devolver;
	}
	
	public void setDevolver(boolean devolver) {
		this.devolver = devolver;
	}
}
	
	